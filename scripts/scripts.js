/**
 * Builds hero block and prepends to main in a new section.
 * @param {Element} main The container element
 */
function buildHeroBlock(main) {
  const h1 = main.querySelector('h1');
  const picture = main.querySelector('picture');
  // eslint-disable-next-line no-bitwise
  if (h1 && picture && (h1.compareDocumentPosition(picture) & Node.DOCUMENT_POSITION_PRECEDING)) {
    // Check if h1 or picture is already inside a hero block
    if (h1.closest('.hero') || picture.closest('.hero')) {
      return; // Don't create a duplicate hero block
    }
    const section = document.createElement('div');
    section.append(buildBlock('hero', { elems: [picture, h1] }));
    main.prepend(section);
  }
}

/**
 * load fonts.css and set a session storage flag
 */
async function loadFonts() {
  await loadCSS(`${window.hlx.codeBasePath}/styles/fonts.css`);
  try {
    if (!window.location.hostname.includes('localhost')) sessionStorage.setItem('fonts-loaded', 'true');
  } catch (e) {
    // do nothing
  }
}

/**
 * Adds decorative SVG illustrations before specific sections.
 * @param {Element} main The container element
 */
function buildSectionIllustrations(main) {
  const illustrations = {
    concepto: 'concepto-illustration',
    localización: 'localizacion-illustration',
    contacto: 'contacto-illustration',
    reglamento: 'reglamento-illustration',
    horarios: 'horarios-illustration',
    clasificación: 'clasificacion-illustration',
    faqs: 'faqs-illustration',
  };

  Object.entries(illustrations).forEach(([sectionId, svgName]) => {
    const heading = main.querySelector(`h2#${CSS.escape(sectionId)}`);
    if (heading) {
      const img = document.createElement('img');
      img.src = `${window.hlx.codeBasePath}/icons/${svgName}.svg`;
      img.alt = '';
      img.loading = 'lazy';
      img.classList.add('section-illustration');
      const wrapper = document.createElement('p');
      wrapper.classList.add('section-illustration');
      wrapper.append(img);
      heading.before(wrapper);
    }
  });
}

/**
 * Replaces social media text links with icon images.
 * @param {Element} main The container element
 */
function buildSocialIcons(main) {
  const socialMap = {
    facebook: 'facebook',
    twitter: 'twitter',
    instagram: 'instagram',
  };

  Object.entries(socialMap).forEach(([domain, iconName]) => {
    const link = main.querySelector(`a[href*="${domain}"]`);
    if (link) {
      const img = document.createElement('img');
      img.src = `${window.hlx.codeBasePath}/icons/${iconName}.svg`;
      img.alt = link.textContent.trim();
      img.loading = 'lazy';
      img.classList.add('social-icon');
      link.textContent = '';
      link.append(img);
      link.classList.add('social-link');
    }
  });

  // Remove the " | " separators between social links
  const contactHeading = main.querySelector('h2#contacto');
  if (contactHeading) {
    let sectionDiv = contactHeading;
    while (sectionDiv.parentElement && sectionDiv.parentElement !== main) {
      sectionDiv = sectionDiv.parentElement;
    }
    const socialParagraph = sectionDiv.querySelector('p:has(.social-link)');
    if (socialParagraph) {
      socialParagraph.classList.add('social-links');
      [...socialParagraph.childNodes].forEach((node) => {
        if (node.nodeType === Node.TEXT_NODE) node.remove();
      });
    }
  }
}

/**
 * Builds the fixed bottom section nav and hides toggled sections initially.
 * Sections are identified by their h2 heading IDs.
 * @param {Element} main The container element
 */
function buildSectionNav(main) {
  const navItems = [
    { text: 'Home', target: 'home', icon: 'nav-home' },
    { text: 'Reglamento', target: 'reglamento', icon: 'nav-reglamento' },
    { text: 'Horarios', target: 'horarios', icon: 'nav-horarios' },
    { text: 'Clasificación', target: 'clasificación', icon: 'nav-clasificacion' },
    { text: "FAQ's", target: 'faqs', icon: 'nav-faqs' },
  ];
  const toggledIds = ['reglamento', 'horarios', 'clasificación', 'faqs'];

  // Hide toggled sections before they render (prevents flash)
  toggledIds.forEach((id) => {
    const heading = main.querySelector(`h2#${CSS.escape(id)}`);
    if (heading) {
      let sectionDiv = heading;
      while (sectionDiv.parentElement && sectionDiv.parentElement !== main) {
        sectionDiv = sectionDiv.parentElement;
      }
      sectionDiv.style.display = 'none';
      sectionDiv.dataset.toggledSection = id;
    }
  });

  // Create nav block with links and icon data
  const rows = navItems.map(({ text, target, icon }) => {
    const a = document.createElement('a');
    a.href = `#${target}`;
    a.textContent = text;
    a.dataset.icon = icon;
    return [a];
  });

  const section = document.createElement('div');
  section.append(buildBlock('section-nav', rows));
  main.append(section);
}

/**
 * Builds all synthetic blocks in a container element.
 * @param {Element} main The container element
 */
function buildAutoBlocks(main) {
  try {
    // auto load `*/fragments/*` references
    const fragments = [...main.querySelectorAll('a[href*="/fragments/"]')].filter((f) => !f.closest('.fragment'));
    if (fragments.length > 0) {
      // eslint-disable-next-line import/no-cycle
      import('../blocks/fragment/fragment.js').then(({ loadFragment }) => {
        fragments.forEach(async (fragment) => {
          try {
            const { pathname } = new URL(fragment.href);
            const frag = await loadFragment(pathname);
            fragment.parentElement.replaceWith(...frag.children);
          } catch (error) {
            // eslint-disable-next-line no-console
            console.error('Fragment loading failed', error);
          }
        });
      });
    }

    buildHeroBlock(main);
    buildSectionIllustrations(main);
    buildSocialIcons(main);
    buildSectionNav(main);
  } catch (error) {
    // eslint-disable-next-line no-console
    console.error('Auto Blocking failed', error);
  }
}

/**
 * Decorates the main element.
 * @param {Element} main The main element
 */
// eslint-disable-next-line import/prefer-default-export
export function decorateMain(main) {
  // hopefully forward compatible button decoration
  decorateButtons(main);
  decorateIcons(main);
  buildAutoBlocks(main);
  decorateSections(main);
  decorateBlocks(main);
}

/**
 * Loads everything needed to get to LCP.
 * @param {Element} doc The container element
 */
async function loadEager(doc) {
  document.documentElement.lang = 'en';
  decorateTemplateAndTheme();
  const main = doc.querySelector('main');
  if (main) {
    decorateMain(main);
    document.body.classList.add('appear');
    await loadSection(main.querySelector('.section'), waitForFirstImage);
  }
  
  await runExperimentation(doc, experimentationConfig);
  
  try {
    /* if desktop (proxy for fast connection) or fonts already loaded, load fonts.css */
    if (window.innerWidth >= 900 || sessionStorage.getItem('fonts-loaded')) {
      loadFonts();
    }
  } catch (e) {
    // do nothing
  }
}

/**
 * Loads everything that doesn't need to be delayed.
 * @param {Element} doc The container element
 */
async function loadLazy(doc) {
  loadHeader(doc.querySelector('header'));

  const main = doc.querySelector('main');
  await loadSections(main);

  const { hash } = window.location;
  const element = hash ? doc.getElementById(hash.substring(1)) : false;
  if (hash && element) element.scrollIntoView();

  loadFooter(doc.querySelector('footer'));

  loadCSS(`${window.hlx.codeBasePath}/styles/lazy-styles.css`);
  loadFonts();
}

/**
 * Loads everything that happens a lot later,
 * without impacting the user experience.
 */
function loadDelayed() {
  // eslint-disable-next-line import/no-cycle
  window.setTimeout(() => import('./delayed.js'), 3000);
  // load anything that can be postponed to the latest here
}

async function loadPage() {
  await loadEager(document);
  await loadLazy(document);
  loadDelayed();
}

loadPage();

async function loadSidekick() {
  if (document.querySelector('aem-sidekick')) {
    import('./sidekick.js');
    return;
  }

  document.addEventListener('sidekick-ready', () => {
    import('./sidekick.js');
  });
}

